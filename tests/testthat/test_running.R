#Test all is running without error
context("Test running without error")

test_that("Basic functions are running without error.", {
  set.seed(0)
  expect_silent(tree<-simCoal(seq(2000,2020,1),function(t){return(5)}))
  expect_is(tree,'phylo')
  expect_silent(res<-detectImportsFAST(tree,constant=T))
  expect_is(res,'resDetectImports')
  expect_silent(res<-detectImportsFAST(tree))
  expect_is(res,'resDetectImports')
  expect_silent(res<-detectImportsFAST(tree,online=T))
  expect_is(res,'resDetectImports')
  expect_silent(invisible(capture.output(res<-detectImportsESD(tree,showPlot=T))))
  expect_is(res,'resDetectImports')
  expect_silent(suppressWarnings(res<-detectImports(tree,constant=T,verbose=F,iter=100,nchains=1)))
  expect_is(res,'resDetectImports')
  expect_silent(suppressWarnings(res<-detectImports(tree,constant=F,verbose=F,iter=100,nchains=1)))
  expect_is(res,'resDetectImports')
})
